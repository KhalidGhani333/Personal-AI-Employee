def main():
    """Main cloud processing loop"""
    print("🌥️  Cloud Processor Started")
    print(f"Time: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("")

    # Process email tasks
    email_tasks = list(NEEDS_ACTION_EMAIL.glob("*.md"))
    if email_tasks:
        print(f"📧 Processing {len(email_tasks)} email task(s)...")
        for task_file in email_tasks:
            process_email_task(task_file)
    else:
        print("📧 No email tasks to process")

    print("")

    # Process social tasks
    social_tasks = list(NEEDS_ACTION_SOCIAL.glob("*.md"))
    if social_tasks:
        print(f"📱 Processing {len(social_tasks)} social task(s)...")
        for task_file in social_tasks:
            process_social_task(task_file)
    else:
        print("📱 No social tasks to process")

    print("")
    print("✅ Cloud processing complete")


if __name__ == "__main__":
    main()
