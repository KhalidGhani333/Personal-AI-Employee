def generate_social_draft(task_content):
    """Generate AI-powered social media draft"""
    # Placeholder - integrate with your AI logic

    return """🚀 Exciting update!

[Your social media content here]

#AI #Automation #Productivity"""


def process_email_task(task_file):
    """
    Process email task:
    1. Claim by moving to In_Progress/cloud
    2. Generate draft reply
    3. Write to Pending_Approval/email
    4. Archive original to Done
    """
    claimed_path = claim_task(task_file, IN_PROGRESS_CLOUD)
    if not claimed_path:
        return

    try:
        # Read task
        with open(claimed_path, 'r', encoding='utf-8') as f:
            task_content = f.read()

        # Generate draft
        draft_reply = generate_email_draft(task_content)

        # Create approval file
        approval_file = PENDING_APPROVAL_EMAIL / f"APPROVAL_{task_file.name}"
        with open(approval_file, 'w', encoding='utf-8') as f:
            f.write(f"""---
status: pending
created: {datetime.now().isoformat()}
task_type: email_reply
cloud_processed: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
---

# Email Reply Approval

## Original Task
{task_content}

## Draft Reply
{draft_reply}

## Actions
- [ ] Approve: Change `status: pending` to `status: approved`
- [ ] Reject: Change `status: pending` to `status: rejected`
- [ ] Edit: Modify the draft reply above

## Instructions
1. Review the draft reply carefully
2. Edit the draft if needed
3. Change status to 'approved' or 'rejected'
4. Save this file
5. Local executor will handle the rest

---
*Generated by Cloud Processor at {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}*
""")

        print(f"✅ Draft created: {approval_file.name}")

        # Archive original
        done_path = DONE / "email" / task_file.name
        done_path.parent.mkdir(parents=True, exist_ok=True)
        shutil.move(str(claimed_path), str(done_path))
        print(f"✅ Archived: {task_file.name}")

    except Exception as e:
        print(f"❌ Error processing {task_file.name}: {e}")
        # Move to Failed
        failed_path = FAILED_CLOUD / task_file.name
        if claimed_path.exists():
            shutil.move(str(claimed_path), str(failed_path))
        print(f"❌ Moved to Failed: {task_file.name}")


